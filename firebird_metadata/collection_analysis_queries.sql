/* 12. Receipt range and header-vs-detail reconciliation */
SELECT
    CAST(p.PAYMENTDATE AS DATE) AS COLLECTION_DATE,
    p.RECEIPTNO,
    p.PAYMENT_ID,
    p.PAYGROUP_CT,
    p.PAIDBY,
    p.AMOUNT AS HEADER_AMOUNT,
    SUM(pd.AMOUNTPAID) AS DETAIL_AMOUNT,
    p.AMOUNT - SUM(pd.AMOUNTPAID) AS DIFFERENCE_AMOUNT
FROM PAYMENT p
JOIN PAYMENTDETAIL pd ON pd.PAYMENT_ID = p.PAYMENT_ID
WHERE p.PAYMENTDATE >= CAST(:date_from AS DATE)
  AND p.PAYMENTDATE < DATEADD(1 DAY TO CAST(:date_to AS DATE))
  AND COALESCE(p.VOID_BV, 0) = 0
GROUP BY CAST(p.PAYMENTDATE AS DATE), p.RECEIPTNO, p.PAYMENT_ID,
         p.PAYGROUP_CT, p.PAIDBY, p.AMOUNT
ORDER BY COLLECTION_DATE, p.RECEIPTNO;

