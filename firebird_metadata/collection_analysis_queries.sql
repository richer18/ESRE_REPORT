LEFT JOIN T_ITAXTYPE it ON it.CODE = pd.ITAXTYPE_CT
WHERE (pd.SOURCE_CT IN ('CTCI', 'CTCC') OR pd.ITAXTYPE_CT = 'CTC')
  AND p.PAYMENTDATE >= CAST(:date_from AS DATE)
  AND p.PAYMENTDATE < DATEADD(1 DAY TO CAST(:date_to AS DATE))
  AND COALESCE(p.VOID_BV, 0) = 0
ORDER BY p.PAYMENTDATE, p.RECEIPTNO, ctc.CTCNO;

/* 5. CTC totals; compares payment line totals to CTC computed/recorded totals */
SELECT
    CAST(p.PAYMENTDATE AS DATE) AS COLLECTION_DATE,
    ctc.CTCTYPE,
    ctc.CTCYEAR,
    COUNT(DISTINCT p.PAYMENT_ID) AS RECEIPT_COUNT,
    COUNT(DISTINCT ctc.CTC_ID) AS CTC_COUNT,
    SUM(pd.AMOUNTPAID) AS PAYMENTDETAIL_TOTAL,
    SUM(ctc.TOTALAMOUNTPAID) AS CTC_RECORDED_TOTAL,
    SUM(
        COALESCE(ctc.BASICTAXDUE, 0)
      + COALESCE(ctc.BUSTAXDUE, 0)
      + COALESCE(ctc.SALTAXDUE, 0)
      + COALESCE(ctc.RPTAXDUE, 0)
      + COALESCE(ctc.INTEREST, 0)
    ) AS CTC_COMPONENT_TOTAL
FROM PAYMENT p
JOIN PAYMENTDETAIL pd ON pd.PAYMENT_ID = p.PAYMENT_ID
LEFT JOIN COMMUNITYTAXCERTIFICATE ctc
       ON ctc.CTC_ID = pd.SOURCEID
      AND pd.SOURCE_CT IN ('CTCI', 'CTCC')
WHERE (pd.SOURCE_CT IN ('CTCI', 'CTCC') OR pd.ITAXTYPE_CT = 'CTC')
  AND p.PAYMENTDATE >= CAST(:date_from AS DATE)
  AND p.PAYMENTDATE < DATEADD(1 DAY TO CAST(:date_to AS DATE))
  AND COALESCE(p.VOID_BV, 0) = 0
GROUP BY CAST(p.PAYMENTDATE AS DATE), ctc.CTCTYPE, ctc.CTCYEAR
ORDER BY COLLECTION_DATE, ctc.CTCTYPE, ctc.CTCYEAR;

