/* ZAMBOANGUITA.FDB collection analysis queries
   Read-only SELECT statements only.
   Parameters used:
     :date_from  -- inclusive date, e.g. DATE '2026-01-01'
     :date_to    -- inclusive date, e.g. DATE '2026-01-31'

   Notes:
   - PAYMENT is the payment/receipt header.
   - PAYMENTDETAIL is the line-level revenue/fund detail.
   - PAYMENTCLASSDETAIL is especially useful for RPT class/tax-year breakdowns.
   - Receipt number is PAYMENT.RECEIPTNO.
   - Void/cancelled filters are written defensively; confirm local status codes before production use.
*/

/* 1. Unified collection detail: RPT, CTC, and Other Fees/Charges */
SELECT
    CASE
        WHEN p.PAYGROUP_CT = 'RPT' THEN 'REAL_PROPERTY_TAX'
        WHEN pd.SOURCE_CT IN ('CTCI', 'CTCC') OR pd.ITAXTYPE_CT = 'CTC' THEN 'COMMUNITY_TAX'
        ELSE 'OTHER_FEES_CHARGES'
    END AS PROCESS_NAME,
    p.PAYMENT_ID,
    p.RECEIPTNO,
    p.PAYMENTDATE,
    p.VALUEDATE,
    p.LOCAL_TIN,
    tp.OWNERNAME,
    p.PAIDBY,
    p.COLLECTOR,
    p.USERID,
    p.RCDNUMBER,
    p.ORB_ID,
    p.PAYGROUP_CT,
    p.PAYMODE_CT,
    pm.DESCRIPTION AS PAYMODE_DESCRIPTION,
    p.STATUS_CT,
    st.DESCRIPTION AS STATUS_DESCRIPTION,
    pd.PAYMENTDETAIL_ID,
    pd.ITAXTYPE_CT,
    it.DESCRIPTION AS ITAXTYPE_DESCRIPTION,
    pd.FUNDTYPE_CT,
    ft.DESCRIPTION AS FUNDTYPE_DESCRIPTION,
    pd.SOURCE_CT,
    pd.SOURCEID,
    pd.DEBITPOSTINGID,
    pd.AMOUNTPAID,
    p.AMOUNT AS PAYMENT_HEADER_AMOUNT
FROM PAYMENT p
JOIN PAYMENTDETAIL pd ON pd.PAYMENT_ID = p.PAYMENT_ID
LEFT JOIN TAXPAYER tp ON tp.LOCAL_TIN = p.LOCAL_TIN
LEFT JOIN T_ITAXTYPE it ON it.CODE = pd.ITAXTYPE_CT
LEFT JOIN T_FUNDTYPE ft ON ft.CODE = pd.FUNDTYPE_CT
LEFT JOIN T_PAYMODE pm ON pm.CODE = p.PAYMODE_CT
LEFT JOIN T_STATUS st ON st.CODE = p.STATUS_CT
WHERE p.PAYMENTDATE >= CAST(:date_from AS DATE)
  AND p.PAYMENTDATE < DATEADD(1 DAY TO CAST(:date_to AS DATE))
  AND COALESCE(p.VOID_BV, 0) = 0
ORDER BY p.PAYMENTDATE, p.RECEIPTNO, pd.RECEIPTITEMORDER, pd.PAYMENTDETAIL_ID;

/* 2. Real Property Tax payment detail with RPT posting/account context */
SELECT
    p.PAYMENT_ID,
    p.RECEIPTNO,
    p.PAYMENTDATE,
    p.LOCAL_TIN,
    tp.OWNERNAME,
    p.PAIDBY,
    p.COLLECTOR,
    pd.PAYMENTDETAIL_ID,
    pd.ITAXTYPE_CT,
    it.DESCRIPTION AS ITAXTYPE_DESCRIPTION,
    pd.FUNDTYPE_CT,
    pd.AMOUNTPAID,
    pd.DEBITPOSTINGID,
    tpa.TAXTRANS_ID,
    tpa.PROP_ID,
    tpa.TAXYEAR,
    tpa.TAXPERIOD_CT,
    tpa.CASETYPE_CT,
    tpa.EARMARK_CT,
    tpa.EVENTOBJECT_CT,
    pcd.CLASSCODE_CT,
    pcd.PROPERTYKIND_CT,
    pcd.ACTUALUSE_CT,
    pcd.TAXYEAR AS CLASS_TAXYEAR,
    pcd.AMOUNT AS CLASS_AMOUNT
FROM PAYMENT p
JOIN PAYMENTDETAIL pd ON pd.PAYMENT_ID = p.PAYMENT_ID
LEFT JOIN PAYMENTCLASSDETAIL pcd
       ON pcd.PAYMENT_ID = p.PAYMENT_ID
      AND pcd.ITAXTYPE_CT = pd.ITAXTYPE_CT
LEFT JOIN TPACCOUNT tpa ON tpa.POSTING_ID = pd.DEBITPOSTINGID
LEFT JOIN TAXPAYER tp ON tp.LOCAL_TIN = p.LOCAL_TIN
LEFT JOIN T_ITAXTYPE it ON it.CODE = pd.ITAXTYPE_CT
WHERE p.PAYGROUP_CT = 'RPT'
  AND p.PAYMENTDATE >= CAST(:date_from AS DATE)
  AND p.PAYMENTDATE < DATEADD(1 DAY TO CAST(:date_to AS DATE))
  AND COALESCE(p.VOID_BV, 0) = 0
ORDER BY p.PAYMENTDATE, p.RECEIPTNO, tpa.TAXYEAR, pd.ITAXTYPE_CT;

/* 3. RPT totals by Basic/SEF/other RPT line classification */
SELECT
    CAST(p.PAYMENTDATE AS DATE) AS COLLECTION_DATE,
    pd.ITAXTYPE_CT,
    it.DESCRIPTION AS ITAXTYPE_DESCRIPTION,
    pd.FUNDTYPE_CT,
    COUNT(DISTINCT p.PAYMENT_ID) AS RECEIPT_COUNT,
    SUM(pd.AMOUNTPAID) AS TOTAL_AMOUNT
FROM PAYMENT p
JOIN PAYMENTDETAIL pd ON pd.PAYMENT_ID = p.PAYMENT_ID
LEFT JOIN T_ITAXTYPE it ON it.CODE = pd.ITAXTYPE_CT
WHERE p.PAYGROUP_CT = 'RPT'
  AND p.PAYMENTDATE >= CAST(:date_from AS DATE)
  AND p.PAYMENTDATE < DATEADD(1 DAY TO CAST(:date_to AS DATE))
  AND COALESCE(p.VOID_BV, 0) = 0
GROUP BY CAST(p.PAYMENTDATE AS DATE), pd.ITAXTYPE_CT, it.DESCRIPTION, pd.FUNDTYPE_CT
ORDER BY COLLECTION_DATE, pd.ITAXTYPE_CT;

/* 4. Community Tax Certificate / Cedula payment detail */
SELECT
    p.PAYMENT_ID,
    p.RECEIPTNO,
    p.PAYMENTDATE,
    p.LOCAL_TIN,
    tp.OWNERNAME,
    p.PAIDBY,
    p.COLLECTOR,
    pd.PAYMENTDETAIL_ID,
    pd.ITAXTYPE_CT,
    it.DESCRIPTION AS ITAXTYPE_DESCRIPTION,
    pd.SOURCE_CT,
    pd.SOURCEID,
    pd.AMOUNTPAID,
    ctc.CTC_ID,
    ctc.CTCNO,
    ctc.DATEISSUED,
    ctc.CTCTYPE,
    ctc.CTCYEAR,
    ctc.BASICTAXDUE,
    ctc.BUSTAXDUE,
    ctc.SALTAXDUE,
    ctc.RPTAXDUE,
    ctc.INTEREST,
    ctc.TOTALAMOUNTPAID
FROM PAYMENT p
JOIN PAYMENTDETAIL pd ON pd.PAYMENT_ID = p.PAYMENT_ID
LEFT JOIN COMMUNITYTAXCERTIFICATE ctc
       ON ctc.CTC_ID = pd.SOURCEID
