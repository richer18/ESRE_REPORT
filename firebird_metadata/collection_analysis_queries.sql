/* 14. Other Fees and Charges parent-child hierarchy with rates */
SELECT
    lvl1.CODE AS LEVEL1_CODE,
    lvl1.DESCRIPTION AS LEVEL1_DESCRIPTION,
    lvl2.CODE AS LEVEL2_CODE,
    lvl2.DESCRIPTION AS LEVEL2_DESCRIPTION,
    lvl3.CODE AS LEVEL3_CODE,
    lvl3.DESCRIPTION AS LEVEL3_DESCRIPTION,
    lvl4.CODE AS LEVEL4_CODE,
    lvl4.DESCRIPTION AS LEVEL4_DESCRIPTION,
    lvl5.CODE AS LEVEL5_CODE,
    lvl5.DESCRIPTION AS LEVEL5_DESCRIPTION,
    child.CODE AS CHARGE_CODE,
    child.DESCRIPTION AS CHARGE_DESCRIPTION,
    child.PARENTCODE AS CHARGE_PARENT_CODE,
    child.TREELEVEL AS CHARGE_TREELEVEL,
    child.FUNDTYPE_CT,
    ft.DESCRIPTION AS FUNDTYPE_DESCRIPTION,
    child.BLGF_CODE,
    child.COA_CODE,
    opr.OPRATE_ID,
    opr.OPGROUP,
    opr.OPSUBGROUP,
    opr.DESCRIPTION AS RATE_DESCRIPTION,
    opr.RATE,
    opr.YEARSTART,
    opr.VALIDFROM,
    opr.ORDINANCENO,
    opr.MUNICIPAL_ID,
    opr.EDITABLERATE_BV
FROM T_ITAXTYPE child
LEFT JOIN T_ITAXTYPE lvl5 ON lvl5.CODE = child.PARENTCODE
LEFT JOIN T_ITAXTYPE lvl4 ON lvl4.CODE = lvl5.PARENTCODE
LEFT JOIN T_ITAXTYPE lvl3 ON lvl3.CODE = lvl4.PARENTCODE
LEFT JOIN T_ITAXTYPE lvl2 ON lvl2.CODE = lvl3.PARENTCODE
LEFT JOIN T_ITAXTYPE lvl1 ON lvl1.CODE = lvl2.PARENTCODE
LEFT JOIN T_FUNDTYPE ft ON ft.CODE = child.FUNDTYPE_CT
LEFT JOIN T_OTHERPAYMENTRATE opr ON opr.ITAXTYPE_CT = child.CODE
WHERE child.CODE NOT IN ('RPT', 'BSC', 'SEF', 'CTC')
  AND (
        child.CODE IN (SELECT DISTINCT ITAXTYPE_CT FROM T_OTHERPAYMENTRATE WHERE ITAXTYPE_CT IS NOT NULL)
        OR child.CODE IN (
            SELECT DISTINCT pd.ITAXTYPE_CT
            FROM PAYMENT p
            JOIN PAYMENTDETAIL pd ON pd.PAYMENT_ID = p.PAYMENT_ID
            WHERE COALESCE(p.PAYGROUP_CT, '') <> 'RPT'
              AND NOT (pd.SOURCE_CT IN ('CTCI', 'CTCC') OR pd.ITAXTYPE_CT = 'CTC')
              AND p.PAYMENTDATE >= CAST(:date_from AS DATE)
              AND p.PAYMENTDATE < DATEADD(1 DAY TO CAST(:date_to AS DATE))
              AND COALESCE(p.VOID_BV, 0) = 0
        )
      )
  AND (opr.OPRATE_ID IS NULL
       OR (
            (opr.VALIDFROM IS NULL OR opr.VALIDFROM < DATEADD(1 DAY TO CAST(:date_to AS DATE)))
            AND (opr.YEARSTART IS NULL OR opr.YEARSTART <= EXTRACT(YEAR FROM CAST(:date_to AS DATE)))
          ))
ORDER BY
    COALESCE(lvl1.CODE, ''),
    COALESCE(lvl2.CODE, ''),
    COALESCE(lvl3.CODE, ''),
    COALESCE(lvl4.CODE, ''),
    COALESCE(lvl5.CODE, ''),
    child.CODE,
    opr.OPGROUP,
    opr.OPSUBGROUP,
    opr.DESCRIPTION;

/* 15. Total collection per collector: daily, monthly, and yearly */
SELECT
    x.PERIOD_TYPE,
    x.COLLECTION_YEAR,
    x.COLLECTION_MONTH,
    x.COLLECTION_DATE,
    x.COLLECTOR,
    x.USERID,
    x.RECEIPT_COUNT,
    x.TOTAL_AMOUNT
FROM (
    SELECT
        'DAILY' AS PERIOD_TYPE,
        EXTRACT(YEAR FROM p.PAYMENTDATE) AS COLLECTION_YEAR,
        EXTRACT(MONTH FROM p.PAYMENTDATE) AS COLLECTION_MONTH,
        CAST(p.PAYMENTDATE AS DATE) AS COLLECTION_DATE,
        COALESCE(NULLIF(TRIM(p.COLLECTOR), ''), TRIM(p.USERID), 'UNSPECIFIED') AS COLLECTOR,
        p.USERID,
        COUNT(DISTINCT p.PAYMENT_ID) AS RECEIPT_COUNT,
        SUM(pd.AMOUNTPAID) AS TOTAL_AMOUNT,
        1 AS SORT_ORDER
    FROM PAYMENT p
    JOIN PAYMENTDETAIL pd ON pd.PAYMENT_ID = p.PAYMENT_ID
    WHERE p.PAYMENTDATE >= CAST(:date_from AS DATE)
      AND p.PAYMENTDATE < DATEADD(1 DAY TO CAST(:date_to AS DATE))
      AND COALESCE(p.VOID_BV, 0) = 0
    GROUP BY
        EXTRACT(YEAR FROM p.PAYMENTDATE),
        EXTRACT(MONTH FROM p.PAYMENTDATE),
        CAST(p.PAYMENTDATE AS DATE),
        COALESCE(NULLIF(TRIM(p.COLLECTOR), ''), TRIM(p.USERID), 'UNSPECIFIED'),
        p.USERID

    UNION ALL

    SELECT
        'MONTHLY' AS PERIOD_TYPE,
        EXTRACT(YEAR FROM p.PAYMENTDATE) AS COLLECTION_YEAR,
        EXTRACT(MONTH FROM p.PAYMENTDATE) AS COLLECTION_MONTH,
        CAST(NULL AS DATE) AS COLLECTION_DATE,
        COALESCE(NULLIF(TRIM(p.COLLECTOR), ''), TRIM(p.USERID), 'UNSPECIFIED') AS COLLECTOR,
        p.USERID,
        COUNT(DISTINCT p.PAYMENT_ID) AS RECEIPT_COUNT,
        SUM(pd.AMOUNTPAID) AS TOTAL_AMOUNT,
        2 AS SORT_ORDER
    FROM PAYMENT p
    JOIN PAYMENTDETAIL pd ON pd.PAYMENT_ID = p.PAYMENT_ID
    WHERE p.PAYMENTDATE >= CAST(:date_from AS DATE)
      AND p.PAYMENTDATE < DATEADD(1 DAY TO CAST(:date_to AS DATE))
      AND COALESCE(p.VOID_BV, 0) = 0
    GROUP BY
        EXTRACT(YEAR FROM p.PAYMENTDATE),
        EXTRACT(MONTH FROM p.PAYMENTDATE),
        COALESCE(NULLIF(TRIM(p.COLLECTOR), ''), TRIM(p.USERID), 'UNSPECIFIED'),
        p.USERID

    UNION ALL

    SELECT
        'YEARLY' AS PERIOD_TYPE,
        EXTRACT(YEAR FROM p.PAYMENTDATE) AS COLLECTION_YEAR,
        CAST(NULL AS INTEGER) AS COLLECTION_MONTH,
        CAST(NULL AS DATE) AS COLLECTION_DATE,
        COALESCE(NULLIF(TRIM(p.COLLECTOR), ''), TRIM(p.USERID), 'UNSPECIFIED') AS COLLECTOR,
        p.USERID,
        COUNT(DISTINCT p.PAYMENT_ID) AS RECEIPT_COUNT,
        SUM(pd.AMOUNTPAID) AS TOTAL_AMOUNT,
        3 AS SORT_ORDER
    FROM PAYMENT p
    JOIN PAYMENTDETAIL pd ON pd.PAYMENT_ID = p.PAYMENT_ID
    WHERE p.PAYMENTDATE >= CAST(:date_from AS DATE)
      AND p.PAYMENTDATE < DATEADD(1 DAY TO CAST(:date_to AS DATE))
      AND COALESCE(p.VOID_BV, 0) = 0
    GROUP BY
        EXTRACT(YEAR FROM p.PAYMENTDATE),
        COALESCE(NULLIF(TRIM(p.COLLECTOR), ''), TRIM(p.USERID), 'UNSPECIFIED'),
        p.USERID
) x
ORDER BY
    x.SORT_ORDER,
    x.COLLECTION_YEAR,
    x.COLLECTION_MONTH,
    x.COLLECTION_DATE,
    x.COLLECTOR,
    x.USERID;

/* 16. Fees collected by selected collector */
SELECT
    COALESCE(NULLIF(TRIM(p.COLLECTOR), ''), TRIM(p.USERID), 'UNSPECIFIED') AS COLLECTOR,
    p.USERID,
    CASE
        WHEN p.PAYGROUP_CT = 'RPT' THEN 'REAL_PROPERTY_TAX'
        WHEN pd.SOURCE_CT IN ('CTCI', 'CTCC') OR pd.ITAXTYPE_CT = 'CTC' THEN 'COMMUNITY_TAX'
        ELSE 'OTHER_FEES_CHARGES'
    END AS PROCESS_NAME,
    p.PAYGROUP_CT,
    pd.ITAXTYPE_CT,
    it.DESCRIPTION AS FEE_DESCRIPTION,
    pd.FUNDTYPE_CT,
    ft.DESCRIPTION AS FUNDTYPE_DESCRIPTION,
    pd.SOURCE_CT,
    COUNT(DISTINCT p.PAYMENT_ID) AS RECEIPT_COUNT,
    MIN(p.RECEIPTNO) AS FIRST_RECEIPTNO,
    MAX(p.RECEIPTNO) AS LAST_RECEIPTNO,
    MIN(CAST(p.PAYMENTDATE AS DATE)) AS FIRST_PAYMENT_DATE,
    MAX(CAST(p.PAYMENTDATE AS DATE)) AS LAST_PAYMENT_DATE,
    SUM(pd.AMOUNTPAID) AS TOTAL_AMOUNT
FROM PAYMENT p
JOIN PAYMENTDETAIL pd ON pd.PAYMENT_ID = p.PAYMENT_ID
LEFT JOIN T_ITAXTYPE it ON it.CODE = pd.ITAXTYPE_CT
LEFT JOIN T_FUNDTYPE ft ON ft.CODE = pd.FUNDTYPE_CT
WHERE p.PAYMENTDATE >= CAST(:date_from AS DATE)
  AND p.PAYMENTDATE < DATEADD(1 DAY TO CAST(:date_to AS DATE))
  AND COALESCE(p.VOID_BV, 0) = 0
  AND (
        :collector IS NULL
        OR UPPER(TRIM(p.COLLECTOR)) = UPPER(TRIM(:collector))
        OR UPPER(TRIM(p.USERID)) = UPPER(TRIM(:collector))
      )
GROUP BY
    COALESCE(NULLIF(TRIM(p.COLLECTOR), ''), TRIM(p.USERID), 'UNSPECIFIED'),
    p.USERID,
    CASE
        WHEN p.PAYGROUP_CT = 'RPT' THEN 'REAL_PROPERTY_TAX'
        WHEN pd.SOURCE_CT IN ('CTCI', 'CTCC') OR pd.ITAXTYPE_CT = 'CTC' THEN 'COMMUNITY_TAX'
        ELSE 'OTHER_FEES_CHARGES'
    END,
    p.PAYGROUP_CT,
    pd.ITAXTYPE_CT,
    it.DESCRIPTION,
    pd.FUNDTYPE_CT,
    ft.DESCRIPTION,
    pd.SOURCE_CT
ORDER BY
    COLLECTOR,
    PROCESS_NAME,
    pd.ITAXTYPE_CT,
    pd.SOURCE_CT;

