    EXTRACT(YEAR FROM p.PAYMENTDATE) AS COLLECTION_YEAR,
    CAST(((EXTRACT(MONTH FROM p.PAYMENTDATE) - 1) / 3) + 1 AS INTEGER) AS COLLECTION_QUARTER,
    CASE
        WHEN p.PAYGROUP_CT = 'RPT' THEN 'REAL_PROPERTY_TAX'
        WHEN pd.SOURCE_CT IN ('CTCI', 'CTCC') OR pd.ITAXTYPE_CT = 'CTC' THEN 'COMMUNITY_TAX'
        ELSE 'OTHER_FEES_CHARGES'
    END AS PROCESS_NAME,
    COUNT(DISTINCT p.PAYMENT_ID) AS RECEIPT_COUNT,
    SUM(pd.AMOUNTPAID) AS TOTAL_AMOUNT
FROM PAYMENT p
JOIN PAYMENTDETAIL pd ON pd.PAYMENT_ID = p.PAYMENT_ID
WHERE p.PAYMENTDATE >= CAST(:date_from AS DATE)
  AND p.PAYMENTDATE < DATEADD(1 DAY TO CAST(:date_to AS DATE))
  AND COALESCE(p.VOID_BV, 0) = 0
GROUP BY EXTRACT(YEAR FROM p.PAYMENTDATE),
         CAST(((EXTRACT(MONTH FROM p.PAYMENTDATE) - 1) / 3) + 1 AS INTEGER),
    CASE
        WHEN p.PAYGROUP_CT = 'RPT' THEN 'REAL_PROPERTY_TAX'
        WHEN pd.SOURCE_CT IN ('CTCI', 'CTCC') OR pd.ITAXTYPE_CT = 'CTC' THEN 'COMMUNITY_TAX'
        ELSE 'OTHER_FEES_CHARGES'
    END
ORDER BY COLLECTION_YEAR, COLLECTION_QUARTER, PROCESS_NAME;

